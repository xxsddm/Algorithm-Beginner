/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */

class Solution {
public:
    void reorderList(ListNode* head) {
        vector<ListNode*> nodes;
        ListNode* prev = head;
        while (prev != nullptr) {
            nodes.push_back(prev);
            prev = prev->next;
        }
        int left = 1, right = (int) nodes.size() - 1; // å¾…åŠ å…¥èŠ‚ç‚¹çš„å·¦å³ç´¢å¼•
        bool front = false; // æ˜¯å¦åŠ å…¥leftæŒ‡é’ˆèŠ‚ç‚¹
        prev = head;    // èµ·å§‹ç«¯ç‚¹ç›´æ¥åŠ å…¥
        while (left <= right) {
            if (front) {
                prev->next = nodes[left++];
                front = false;
            }
            else {
                prev->next = nodes[right--];
                front = true;
            }
            prev = prev->next;
        }
        prev->next = nullptr;
    }
};

