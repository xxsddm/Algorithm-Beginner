class Solution:
    def minDepth(self, root: TreeNode) -> int:    # BFS
        if not root:
            return 0
        nodes = [root]
        count = 0
        while nodes:
            count += 1
            temp = []
            for node in nodes:
                if node.left:
                    temp.append(node.left)
                elif not node.right:
                    return count
                if node.right:
                    temp.append(node.right)
            nodes = temp
        return count
