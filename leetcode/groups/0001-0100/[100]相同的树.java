class Solution {
    public boolean isSameTree(TreeNode p, TreeNode q) {     // è¿­ä»£
        Stack<TreeNode> nodes = new Stack<>();
        TreeNode node1, node2;
        nodes.add(p);
        nodes.add(q);
        while (!nodes.isEmpty()) {
            node1 = nodes.pop();
            node2 = nodes.pop();
            if (node1 != null && node2 == null) {
                return false;
            }
            if (node1 == null && node2 != null) {
                return false;
            }
            if (node1 != null && node2 != null) {
                if (node1.val == node2.val) {
                    nodes.add(node1.left);
                    nodes.add(node2.left);
                    nodes.add(node1.right);
                    nodes.add(node2.right);
                }
                else {
                    return false;
                }
            }
        }
        return true;
    }
}

