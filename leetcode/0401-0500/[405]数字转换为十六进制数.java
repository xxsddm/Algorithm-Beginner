class Solution {
    public String toHex(int num) {
        if (num == 0) {
            return "0";
        }
        StringBuilder ans = new StringBuilder();
        for (int part = 7; part >= 0; part--) {  // intæœ‰32ä½,æ¯4ä½è½¬æ¢ä¸ºä¸€ä¸ªåå…­è¿›åˆ¶æ•°å­—
            int num16 = (num >> (part * 4)) & 15;   // 15=0xf
            if (num16 == 0 && ans.length() == 0) {
                continue;
            }
            if (num16 < 10) {
                ans.append((char) (num16 + '0'));
            }
            else {
                ans.append((char) (num16 - 10 + 'a'));
            }
        }
        return ans.toString();
    }
}

