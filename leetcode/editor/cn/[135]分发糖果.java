class Solution {
    public int candy(int[] ratings) {
        int[] nums = new int[ratings.length];
        for (int i = ratings.length - 1; i > 0; i--) {      // ä»åå‘å‰
            if (ratings[i - 1] > ratings[i]) {
                nums[i - 1] = nums[i] + 1;
            }
        }
        for (int i = 0; i < ratings.length - 1; i++) {      // ä»å‰å‘å
            if (ratings[i + 1] > ratings[i]) {
                // å³°å€¼ç‚¹ä¼šè¢«æ›´æ–°ä¸¤æ¬¡(å–ä¸¤æ¬¡ä¸­çš„è¾ƒå¤§å€¼)
                nums[i + 1] = Math.max(nums[i + 1], nums[i] + 1);
            }
        }
        int count = ratings.length;
        for (int num: nums) {
            count += num;
        }
        return count;
    }
}

