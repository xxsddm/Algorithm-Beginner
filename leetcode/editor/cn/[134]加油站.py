class Solution:
    def canCompleteCircuit(self, gas: List[int], cost: List[int]) -> int:       # æ³¨é‡Šè§java
        ans, start, length = -1, 0, len(gas)
        before = [0, 0, gas[0] - cost[0]]
        while start < length:
            rest = distance = 0
            while distance <= length:
                loc = (start + distance) % length
                rest += gas[loc] - cost[loc]
                if loc == before[0]:
                    distance += before[1]
                    rest += before[2] - (gas[loc] - cost[loc])

                if rest < 0:
                    before[0] = start
                    before[1] = distance
                    before[2] = rest
                    start += distance + 1
                    break

                if distance == length:
                    ans = start
                    return ans

                distance += 1
        return ans

