class Solution:
    def isSameTree(self, p: TreeNode, q: TreeNode) -> bool:     # è¿­ä»£
        nodes = [p, q]
        while nodes:
            node1 = nodes.pop()
            node2 = nodes.pop()
            if not node1 and node2:
                return False
            elif node1 and not node2:
                return False
            elif node1 and node2:
                if node1.val != node2.val:
                    return False
