class Solution {
    public TreeNode searchBST(TreeNode root, int val) {     // BFS
        if (root == null) {
            return null;
        }
        Stack<TreeNode> container = new Stack<>();
        container.add(root);
        while (!container.isEmpty()) {
            TreeNode node = container.pop();
            if (node.val == val) {
                return node;
            }
            if (node.left != null) {
                container.add(node.left);
            }
            if (node.right != null) {
                container.add(node.right);
            }
        }
        return null;
    }
}
