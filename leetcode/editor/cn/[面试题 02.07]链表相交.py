class Solution:
    def getIntersectionNode(self, headA: ListNode, headB: ListNode) -> ListNode:
        part1, part2 = headA, headB
        l1, l2 = [], []
        while part1:
            l1.append(part1)
            part1 = part1.next
        while part2:
            l2.append(part2)
            part2 = part2.next
        if l1.__len__() == 0 or l2.__len__() == 0:
            return None
        part1 = l1.pop()
        part2 = l2.pop()
        if part1 != part2:
            return None
        back = part1
        while l1 and l2:
            part1 = l1.pop()
            part2 = l2.pop()
            if part1 != part2:
                return back
            back = part1
        return back
