    def getIntersectionNode(self, headA: ListNode, headB: ListNode) -> ListNode:
        part1, part2 = headA, headB
        len1, len2 = 0, 0
        while part1:
            part1 = part1.next
            len1 += 1
        while part2:
            part2 = part2.next
            len2 += 1
        part1, part2 = headA, headB
        while len1 != len2:
            if len1 > len2:
                part1 = part1.next
                len1 -= 1
            else:
                part2 = part2.next
                len2 -= 1
        while part1 != part2:
            part1 = part1.next
            part2 = part2.next
        return part1
