# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def reverseBetween(self, head: ListNode, left: int, right: int) -> ListNode:
        if left == right:
            return head
        count = 0
        temp = visualnode = ListNode(next=head)
        while count <= right:
            if count == left - 1:
                templeft = temp
            elif count == left:
                headleft = temp
            elif count == right:
                tailright = temp
            temp = temp.next
            count += 1
        templeft.next = tailright
        before = headleft
        temp = headleft.next
        headleft.next = tailright.next
        while temp != tailright:
            temptemp = temp.next
            temp.next = before
            before = temp
            temp = temptemp
        temp.next = before
        return visualnode.next

