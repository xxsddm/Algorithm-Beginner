/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */


class Solution {
    public ListNode insertionSortList(ListNode head) {      // ç›´æ¥å½’å¹¶ç®—äº†
        if (head == null) {
            return null;
        }
        if (head.next == null) {
            return head;
        }
        ListNode slow = head, fast = head, righthead;
        while (fast.next != null && fast.next.next !=null) {
            fast = fast.next.next;
            slow = slow.next;
        }
        righthead = slow.next;
        slow.next = null;
        return merge(insertionSortList(head), insertionSortList(righthead));
    }

    private ListNode merge(ListNode left, ListNode right) {
        ListNode visualhead = new ListNode(), prev = visualhead;
        while (left != null || right != null) {
            if (left != null && right != null) {
                if (left.val <= right.val) {
                    prev.next = left;
                    prev = prev.next;
                    left = left.next;
                }
                else {
                    prev.next = right;
                    prev = prev.next;
                    right = right.next;
                }
            } else if (right == null) {
                prev.next = left;
                prev = prev.next;
                left = left.next;
            } else {
                prev.next = right;
                prev = prev.next;
                right = right.next;
            }
        }
        return visualhead.next;
    }
}

